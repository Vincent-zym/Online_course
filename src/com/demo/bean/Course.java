package com.demo.bean;

public class Course {
	private Integer course_id;
	private String course_name;
	private String course_tea;
	private Integer course_hour;
	private Float course_mark;
	private String course_time;
	private String course_place;
	
	public Course() {
	}
	
	public Integer getCourse_id() {
		return course_id;
	}
	public void setCourse_id(Integer course_id) {
		this.course_id = course_id;
	}
	public String getCourse_name() {
		return course_name;
	}
	public void setCourse_name(String course_name) {
		this.course_name = course_name;
	}
	public String getCourse_tea() {
		return course_tea;
	}
	public void setCourse_tea(String course_tea) {
		this.course_tea = course_tea;
	}
	public Integer getCourse_hour() {
		return course_hour;
	}
	public void setCourse_hour(Integer course_hour) {
		this.course_hour = course_hour;
	}
	public Float getCourse_mark() {
		return course_mark;
	}
	public void setCourse_mark(Float course_mark) {
		this.course_mark = course_mark;
	}
	public String getCourse_time() {
		return course_time;
	}
	public void setCourse_time(String course_time) {
		this.course_time = course_time;
	}
	public String getCourse_place() {
		return course_place;
	}
	public void setCourse_place(String course_place) {
		this.course_place = course_place;
	}
	
	
}
